
# Sempre no final de um laço de condição ou de repetição, colocamos dois pontos ':'
# 2 pontos significa a criação de um bloco de código
# Quando criamos um novo bloco de código, obrigatoriamente precisamos usar
# indentação nesse novo bloco (padrão de 4 espaços em branco)

if __name__ == "__main__":

    # O jogo da velha representa um comentário em Python
    # print() é uma das funções built-in do python
    print("Olá mundo")

    print("No Python, o final da linha significa o fim de uma sentença")

    # Na linha abaixo, apesar dos prints estarem na mesma linha, os textos
    # serão mostrados em linhas diferentes
    print("Podemos usar o "); print("ponto-e-vírgula para separar as sentenças em uma linha")

    # Python é uma linguagem case-sensitive
    # Ele diferencia caracteres maiúsculos e minúsculos
    nome = "Maria"
